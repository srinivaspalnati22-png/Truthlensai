window.changeLanguage = function (langCode) {
    if (!appTranslations[langCode]) return;
    const dict = appTranslations[langCode];
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (dict[key]) {
            el.innerHTML = dict[key];
        }
    });
    localStorage.setItem('truthLensLang', langCode);
};

// Global Toast
function showToast(message, type = 'info') {
    const container = document.getElementById('toastContainer');
    if (!container) return;
    const toast = document.createElement('div');
    toast.className = `toast flex items-center gap-3 p-4 rounded-xl shadow-2xl border backdrop-blur-md min-w-[300px] bg-slate-800/90 z-[100]`;

    let icon = '<i class="fas fa-info-circle text-primary text-xl"></i>';
    if (type === 'success') { toast.classList.add('border-green-500/50'); icon = '<i class="fas fa-check-circle text-green-500 text-xl"></i>'; }
    if (type === 'error') { toast.classList.add('border-red-500/50'); icon = '<i class="fas fa-exclamation-circle text-red-500 text-xl"></i>'; }

    toast.innerHTML = `${icon} <span class="font-display font-bold text-sm text-white no-invert">${message}</span>`;
    container.appendChild(toast);

    setTimeout(() => {
        toast.classList.add('hide');
        setTimeout(() => toast.remove(), 300);
    }, 4000);
}

