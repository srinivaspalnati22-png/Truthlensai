let chatSpeechRec = null;

function initChatbot() {
    // Native Lang Mapping for Voice Input/Output
    const langMap = { 'en': 'en-US', 'hi': 'hi-IN', 'te': 'te-IN', 'ta': 'ta-IN', 'bn': 'bn-IN' };

    window.handleUserChatMessage = async function () {
        const input = document.getElementById('aiChatInput');
        const msg = input.value.trim();
        const currentLang = localStorage.getItem('truthLensLang') || 'en';

        if (!msg) return;
        appendChatBubble(msg, 'user', currentLang);
        input.value = '';
        const tid = addChatTypingIndicator();
        try {
            const res = await fetch('/chat', { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ message: msg, language: currentLang }) });
            const data = await res.json();
            document.getElementById(tid).remove();
            appendChatBubble(data.reply, 'bot', currentLang);
            speakText(data.reply, langMap[currentLang]);
        } catch (e) {
            document.getElementById(tid).remove();
            showToast("Sync Error", "error");
        }
    }

    if (window.SpeechRecognition || window.webkitSpeechRecognition) {
        const SR = window.SpeechRecognition || window.webkitSpeechRecognition;
        chatSpeechRec = new SR();
        chatSpeechRec.continuous = false;
        chatSpeechRec.interimResults = false;
        chatSpeechRec.onresult = (e) => {
            document.getElementById('aiChatInput').value = e.results[0][0].transcript;
            hideChatMicVisualizer();
            handleUserChatMessage();
        };
        chatSpeechRec.onerror = () => hideChatMicVisualizer();
        chatSpeechRec.onend = () => hideChatMicVisualizer();
    }

    document.getElementById('chatMicTriggerBtn')?.addEventListener('click', () => {
        if (!chatSpeechRec) return showToast("Mic not supported in browser", "error");
        const currentLang = localStorage.getItem('truthLensLang') || 'en';
        chatSpeechRec.lang = langMap[currentLang] || 'en-US';
        chatSpeechRec.start();
        showChatMicVisualizer();
        window.speechSynthesis.cancel();
    });
}

function speakText(text, langCode) {
    if (!window.speechSynthesis) return;
    window.speechSynthesis.cancel();
    const synth = new SpeechSynthesisUtterance(text);
    synth.lang = langCode || 'en-US';
    window.speechSynthesis.speak(synth);
}

